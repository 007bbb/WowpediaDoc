-- emote ids in framexml and the dbc dont really match
-- this complicates things a bit
function m:main(options)
	options = Util:GetFlavorOptions(options)

	local cmd_tbl = self:GetCmdTbl()
	local patchData = dbc_patch:GetPatchData("emotestext", options)
	if patch == Util.PtrVersion then
		patch = patch.." {{Test-inline}}"
	end

	local dbc_emotestextdata = {}
	Util:ReadCSV("emotestextdata", parser, options, function(_, ID, l)
		local EmotesTextID = tonumber(l.EmotesTextID)
		local RelationshipFlags = tonumber(l.RelationshipFlags)
		dbc_emotestextdata[EmotesTextID] = dbc_emotestextdata[EmotesTextID] or {}
		dbc_emotestextdata[EmotesTextID][RelationshipFlags] = l.Text_lang
	end)

	local dbc_emotes = {}
	Util:ReadCSV("emotes", parser, options, function(_, ID, l)
		dbc_emotes[ID] = tonumber(l.AnimID)
	end)

	local dbc_emotestextsound = {}
	Util:ReadCSV("emotestextsound", parser, options, function(_, ID, l)
		local EmotesTextID = tonumber(l.EmotesTextID)
		dbc_emotestextsound[EmotesTextID] = true
	end)

	print("writing to "..OUTPUT)
	local file = io.open(OUTPUT, "w")
	file:write('{| class="sortable darktable zebra col3-center col4-center"\n'
	..'! Token !! Slash Command !! Anim !! Voice !! No Target Text !! Targeted Text !! Patch\n')
	local fs = '|-\n| %s || %s || %s || %s || %s || %s || %s\n'

	Util:ReadCSV("emotestext", parser, options, function(_, ID, l)
		local token = l.Name
		local cmd_list = cmd_tbl[token] or "❌"
		if wp_removed[ID] then
			token = "❌ "..token
		end
		if #cmd_list > 28 then
			cmd_list = string.format("<small>%s</small>", cmd_list)
		end
		local EmoteID = tonumber(l.EmoteID)
		local anim = ""
		if EmoteID > 0 then
			anim = string.format('<span title="ID %d">%s</span>', dbc_emotes[EmoteID], "✔️")
		end
		local voice = dbc_emotestextsound[ID] and "✔️" or ""
		local notarget, target = "", ""
		if dbc_emotestextdata[ID] then
			target = dbc_emotestextdata[ID][2] or ""
			notarget = dbc_emotestextdata[ID][6] or ""
		end
		local patch = patchData[ID] and Util:GetPatchVersion(patchData[ID]) or ""
		file:write(fs:format(token, cmd_list, anim, voice, notarget, target, patch))
	end)

	file:write("|}\n")
	file:close()
end

local function ToSet(tbl, vkey)
	local t, hash = {}, {}
	for _, v in pairs(tbl) do
		if not hash[v[vkey]] then
			hash[v[vkey]] = true
			table.insert(t, v[vkey])
		end
	end
	return t
end

function m:GetCmdTbl()
	-- get groups of slash commands
	local t1, t2 = {}, {}
	for cmd_key, cmd_slash in pairs(self.commands) do
		local emote_id, cmd_idx = cmd_key:match("EMOTE(%d+)_CMD(%d)")
		if emote_id then
			t1[emote_id] = t1[emote_id] or {}
			table.insert(t1[emote_id], {cmd_idx, cmd_slash})
		end
	end
	-- sort by cmd index
	for id, cmd_tbl in pairs(t1) do
		table.sort(cmd_tbl, function(a, b)
			return a[1] < b[1]
		end)
		local token = _G["EMOTE"..id.."_TOKEN"]
		local set = ToSet(cmd_tbl, 2)
		t2[token] = table.concat(set, " ")
	end
	return t2
end

